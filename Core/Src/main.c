	      if (ret2 != RCL_RET_OK) {
	      	          printf("Error publishing feedback (line %d)\n", __LINE__);
	      	      }
	      // 6) optional toggle to observe loop
	      HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_15);


